# Operadores lógicos en Ruby

a = true
b = false
puts a && b  # 'and' en Ruby se reemplaza por '&&'

# Operaciones con valores enteros y operadores lógicos
a = 2
b = 3
c = 4
d = 10

puts a == b && c < d  # 'and' en Ruby se reemplaza por '&&'

puts !(a == b) && c > d  # 'not' en Ruby se reemplaza por '!' para negar la expresión
